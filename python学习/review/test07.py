'''百钱百鸡是我国古代数学家张丘建在《算经》一书中提出的数学问题：
鸡翁一值钱五，鸡母一值钱三，鸡雏三值钱一。百钱买百鸡，问鸡翁、鸡母、鸡雏各几何？
翻译成现代文是：公鸡5元一只，母鸡3元一只，小鸡1元三只，用100块钱买一百只鸡，问公鸡、母鸡、小鸡各有多少只？'''
'''
    如果全部买公鸡只能买100/5只
    全部买母鸡 可以买 100/3只
    全部买小鸡 可以买 100*3 只

'''

for i in range(100//5):
    for j in range((100-5*i) // 3):
        n = 100 - i - j
        if(i * 5 + j * 3 +n / 3 == 100):
            print("%d %d %d " %(i,j,n))